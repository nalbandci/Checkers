#pragma once

// Перечисление Response определяет возможные ответы/действия от пользователя или системы
// Используется для обработки событий ввода и управления игровым процессом
enum class Response
{
    OK,      // Действие успешно выполнено, можно продолжать
    BACK,    // Запрос на отмену последнего хода (возврат на шаг назад)
    REPLAY,  // Запрос на перезапуск игры (начать новую игру)
    QUIT,    // Запрос на выход из игры
    CELL     // Пользователь выбрал клетку на доске (нажатие мышки на клетку)
};